	function SWEP:StickToWall()
		local ply = self.Owner
		if not IsValid(ply) or self.Planted then
			return
		end

		local startpos = ply:GetShootPos()
		local end_pos = startpos + ply:GetAimVector()*80
		local tr = util.TraceLine({
			start = startpos,
			endpos = end_pos,
			filter = {ply, self},
			mask = MASK_SOLID
		})

		if tr.HitWorld then
			local melon = ents.Create("ttt_melonmine")
			if IsValid(melon) then
				melon:PointAtEntity(ply)

				local tr_ent = util.TraceEntity({
					start = startpos,
					endpos = end_pos,
					filter = {ply, self}
				}, melon)

				if tr_ent.HitWorld then
					local ang = tr_ent.HitNormal:Angle()
					ang:RotateAroundAxis(ang:Right(), -90)
					ang:RotateAroundAxis(ang:Up(), 180)

					melon:SetPos(tr_ent.HitPos)
					melon:SetAngles(ang)
					melon:SetOwner(ply)
					melon:Spawn()

					melon.fingerprints = self.fingerprints
					melon.Face = self:GetFace()

					melon:EmitSound("weapons/c4/c4_plant.wav")

					local phys = self:GetPhysicsObject()
					if IsValid(phys) then
						phys:EnableMotion(false)
					end

					self:Remove()
				end
				ply:SetAnimation(PLAYER_ATTACK1)
			end
		end
	end
end

